package com.coldline.src.uihandler;
/**
 * interface for enabling and clickable buttons
 * @author adrian erle vega
 *
 */
public interface MultipleButtonClickable {
	/**
	 * abstract method to add available buttons
	 */
	public void addAllClickableButtons();
	/**
	 * method to make buttons clickable
	 */
	public void makeAllButtonsClickable();
}
